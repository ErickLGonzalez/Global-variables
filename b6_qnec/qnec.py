def fabricated_violation(c: Fraction, du: Fraction) -> Tuple[Fraction, Fraction, Fraction]:
    """A 'state' claiming vacuum energy (T = 0) but entropy growing with
    the WRONG concavity: S'' = +c/(6 du^2) (sign flipped). Then
    2 pi T - S'' = -c/(6 du^2) < 0 and the matrix must be rejected with
    an exact witness. Physically: entropy cannot accelerate along a null
    ray without paying energy -- the QNEC gate catching a fake."""
    c, du = Fraction(c), Fraction(du)
    return c / 6, c / (6 * du), -c / (6 * du ** 2)
